package com.devlabs.assignment1;

public class SumOfNaturalNumbers {

	public static void main(String[] args) {
		
		int sum = 0;
		 
        for(int i= 1 ; i<= 20; i++) {
        	
        		sum += i;
        }
        
        System.out.println("Sum Of Natural Numbers: "+sum);
	}

}
